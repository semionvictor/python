import math

def area_of_circle(radius):
    return f'Area of circle with radius {radius} is {math.pi*(radius * radius)}'


def area_of_rectangle(Length,Breath):
    return f"Area of rectangle is  {Length*Breath}"

def areaoftriangle(base, heigth):
    return f"area of triangle is {(base * heigth)/2}"

def areaofsquare(length):
    return f"area of square is {length**2}"
